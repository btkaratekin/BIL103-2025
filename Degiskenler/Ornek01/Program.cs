using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Ornek01
{
    internal class Program
    {
        /*Örnek: Aşağıdaki Değişken Tanımlama Örneklerini İnceleyiniz*/
        static void Main(string[] args)
        {
            int sayi = 10; // Tam sayı türünde değişken tanımlama
            double ondalikSayi = 20.5; // Ondalıklı sayı türünde değişken tanımlama
            string metin = "Merhaba Dünya"; // Metin türünde değişken tanımlama
            bool dogruMu = true; // Mantıksal türünde değişken tanımlama
            char karakter = 'A'; // Karakter türünde değişken tanımlama
            byte veri = 129; // 0-255 aralığında tam sayı türünde değişken tanımlama
            float kucukOndalik = 5.75f; // Küçük ondalıklı sayı türünde değişken tanımlama float tipinde bir değişkene değer ataması yaparken f ekini kullanmalıyız
            long buyukTamSayi = 1234567890; // Büyük tam sayı türünde değişken tanımlama 
            decimal hassasOndalik = 99.99M; // Yüksek hassasiyetli ondalıklı sayı türünde değişken tanımlama decimal tipinde bir değişkene değer ataması yaparken M ekini kullanmalıyız

        }
    }
}
